CREATE TABLE `USER`(
    Username VARCHAR(30) UNIQUE,
    PASSWORD VARCHAR(64),
    PRIMARY KEY(Username)
);
CREATE TABLE ADDRESS(
    HouseNum INT,
    Street VARCHAR(255),
    City VARCHAR(255),
    Province VARCHAR(2),
    PRIMARY KEY(HouseNum)
);
CREATE TABLE ADMINISTRATOR(
    Username VARCHAR(30) UNIQUE,
    PRIVILEGES VARCHAR(30),
    PRIMARY KEY(Username),
    FOREIGN KEY(Username)
 REFERENCES `USER`(Username) 
ON DELETE CASCADE
ON UPDATE CASCADE
);
CREATE TABLE CUSTOMER(
    Username VARCHAR(30) UNIQUE,
    HouseNum INT,
    Postal_CODE VARCHAR(6),
    Email VARCHAR(255) UNIQUE,
    PhoneNum BIGINT UNIQUE,
    PRIMARY KEY(Username),
    FOREIGN KEY(HouseNum) 
REFERENCES ADDRESS(HouseNum) 
ON DELETE CASCADE
ON UPDATE CASCADE,
    FOREIGN KEY(Username)
REFERENCES `USER`(Username)
ON DELETE CASCADE
ON UPDATE CASCADE
);

CREATE TABLE PRODUCTS(
    ProdID INT,
    Color VARCHAR(255),
    Size VARCHAR(3),
    Quantity INT,
    Picture LONGBLOB NOT NULL,
    Price INT,
    Gender VARCHAR(1),
    PRIMARY KEY(ProdID)
);
CREATE TABLE JACKETS(
    ProdID INT,
    JacketType VARCHAR(255),
    PRIMARY KEY(ProdID),
    FOREIGN KEY(ProdID) 
    	REFERENCES PRODUCTS(ProdID) 
    	ON DELETE CASCADE
ON UPDATE CASCADE
);
CREATE TABLE PANTS(
    ProdID INT,
    PantType VARCHAR(255),
    PRIMARY KEY(ProdID),
    FOREIGN KEY(ProdID) 
REFERENCES PRODUCTS(ProdID) 
ON DELETE CASCADE
);
CREATE TABLE SHIRTS(
    ProdID INT,
    ShirtType VARCHAR(255),
    PRIMARY KEY(ProdID),
    FOREIGN KEY(ProdID) 
REFERENCES PRODUCTS(ProdID) 
ON DELETE CASCADE
ON UPDATE CASCADE
);

CREATE TABLE WISHLIST (
    WishID INT,
    Username VARCHAR(255) UNIQUE,
    ProdID INT,
    PRIMARY KEY(WishID),
    FOREIGN KEY(Username) 
REFERENCES `USER`(Username) 
ON DELETE NO ACTION
ON UPDATE CASCADE,
    FOREIGN KEY(ProdID) 
REFERENCES PRODUCTS(ProdID) 
ON DELETE NO ACTION
ON UPDATE CASCADE
);

CREATE TABLE SHOPPINGCART (
    SCID INT,
    Username VARCHAR(255) UNIQUE,
    ProdID INT,
    PRIMARY KEY(SCID),
    FOREIGN KEY(Username)
 REFERENCES `USER`(Username) 
ON DELETE NO ACTION
ON UPDATE CASCADE,
    FOREIGN KEY(ProdID) 
REFERENCES PRODUCTS(ProdID) 
ON DELETE NO ACTION
ON UPDATE CASCADE
);
CREATE TABLE INVOICE(
    IID INT,
    SCID INT,
    Total INT,
    PRIMARY KEY (IID),
    FOREIGN KEY(SCID ) 
REFERENCES SHOPPINGCART(SCID) 
ON UPDATE CASCADE
);
